"""Mathematical utilities for perception components.

This module provides shared mathematical functions used across perception components,
including geometry transformations and coordinate conversions.
"""

import numpy as np


def quaternion_to_rotation_matrix(
    qx: float, qy: float, qz: float, qw: float
) -> np.ndarray:
    """Convert quaternion to rotation matrix (optimized implementation).

    Args:
        qx, qy, qz, qw: Quaternion components

    Returns:
        3x3 rotation matrix as numpy array (dtype=np.float64)
    """
    xx = qx * qx
    yy = qy * qy
    zz = qz * qz
    xy = qx * qy
    xz = qx * qz
    yz = qy * qz
    wx = qw * qx
    wy = qw * qy
    wz = qw * qz

    return np.array(
        [
            [1.0 - 2.0 * (yy + zz), 2.0 * (xy - wz), 2.0 * (xz + wy)],
            [2.0 * (xy + wz), 1.0 - 2.0 * (xx + zz), 2.0 * (yz - wx)],
            [2.0 * (xz - wy), 2.0 * (yz + wx), 1.0 - 2.0 * (xx + yy)],
        ],
        dtype=np.float64,
    )
